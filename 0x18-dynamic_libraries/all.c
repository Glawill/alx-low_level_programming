#include <stdio.h>
/**
 * add - add two interges
 * @a:first unt
 * @b:second int
 * Return additional
 */
int add (int a,int b)
{
	return(a+b);
}		
/**
 * mul-multiply two integers
 * @a:first unt
 * @b:second int
 * Return:multiply
 */
int mul(int a,int b)
{
	return(a*b);
}		
/**
 * mod:mod of two integers
 * @a:first unt
 * @b:second int
 * Return:final answer
 */
int mod(int a,int b)
{
	return(a%b);
}	
